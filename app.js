// Preguntar la temperatura en Celcius
let temperature = prompt('¿Cuál es la temperatura en Celsius?');
// Convertir Celsius a Fahrenheit
let convertToFahrenheit = (temperature*1.8) + 32;
// Mostrar resultado en la consola y en la web
 console.log('La temperatura en Fahrenheit es ' + convertToFahrenheit);
 document.write('La temperatura en Fahrenheit es ' + convertToFahrenheit);